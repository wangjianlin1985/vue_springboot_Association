        <br />&emsp;因而，每一个进行筹备的社团都将接受社员联合会权益部的考核，只有优秀的社团才能走到最后。为了方便社团朋友及审批人员工作的开展，社团联合会权益部特别为大家制作了《社团成立指南》，希望能为你们予以方向性指导，以减少筹备中过多不必要的麻烦。
        <br />&emsp;最后，预祝你们在社团成立及后续的工作中一帆风顺，为我校社团文化做出贡献！
        <br />&emsp;学生社团联合会行政监察部
        <br />&emsp;2013年9月
        <br />&emsp;二 初期准备工作
        <br />&emsp;如果你们有：
        <br />&emsp;1、至少4名社团发起人。
        <br />&emsp;2、社团名称及主旨。
        <br />&emsp;3、对社团活动的初步规划。
        <br />&emsp;4、清晰的社团构想，明确的社团主题。
        <br />&emsp;请联系我们，行政监察部将派出工作人员与发起人进行初步沟通了解，并对初步筹备工作予以帮助指导。
        <br />&emsp;发起人在与行政监察部工作人员初步沟通后，开始着手拟定《社团筹建申请》。申请中应包括发起人对社团的初步构想，如社团的名称、宗旨、性质，及社团活动的初步规划等。
        <br />&emsp;《社团筹建申请》（附上社团发起人简历，及学生证复印件和社团主要成员通讯录）拟定后由发起人交至行政监察部，行政监察部将对申请予以审议，并派出工作人员与发起人进行进一步的筹建洽谈以了解社团的发展前景和发起人对社团发展的看法等。
        <br />&emsp;三 筹建洽谈简介
        <br />&emsp;行政监察在充分研究《社团筹建申请》的基础上，如果社团确有发展前景，将派出工作人员与发起人进行进一步的、面对面的洽谈，筹建洽谈的主要目的是双方交换社团理念并对整体社团成立工作进行说明和帮助指导。
        <br />&emsp;发起人在筹建洽谈前应该仔细思考以下两个问题：
        <br />&emsp;1、为什么发起该社团，社团在学校会有怎样的影响？社团内部有怎样的文化氛围？
        <br />&emsp;2、希望在社团里获得什么？社团又能带给会员什么？
        <br />&emsp;正式洽谈时，社团方面需要至少两名核心发起人到场。筹建洽谈将以沟通交流的方式全面且深入地了解发起人对社团的构想，并且共同探讨适合社团发展的运营模式。希望发起人能在洽谈过程中充分展示其成立协会的热情和发起人自身的组织才能。
        <br />&emsp;洽谈结束后，行政监察部将在认真研讨社团发展前景及发起人的初步规划后，于三天内给出筹建洽谈报告，在报告中确定社团是否有成立的资格。
        <br />&emsp;若社团有成立的资格，行政监察部将对该社团予以进一步帮助和指导并派出一名工作人员作为协会的临时副会长，全面协助协会筹建工作。
        <br />&emsp;四 试运行简介
        <br />&emsp;试运行时间为1至2个月。社团发起人在试运行期间需完成以下工作：
        <br />&emsp;1、管理层的招募与构建。
        <br />&emsp;2、各项规章制度的制定。
        <br />&emsp;3、挂靠院系与指导老师的联系。
        <br />&emsp;4、举办三次或三次以上活动。
        <br />&emsp;5、招收一定数量的的会员（志愿者），并举办一次会员大会对会员所拥有的权利和义务进行说明。
        <br />&emsp;6、归划具有社团特色的品牌活动。
        <br />&emsp;试运行期间，临时副会长将参与协会的各项筹建工作，并负责协会与社联相关的联系等事务。社团可通过临时副会长向社联申请教室、户外场地，或参与社联其他部门对正式社团做的外联干训、财务干训、宣传干训、会长干训等。
        <br />&emsp;社团在使用“华中科技大学XX社团”的称呼时需要在社团名称后注明“筹建”字样。
        <br />&emsp;社团可以招募志愿者（即正式社团的会员）参与试运行期间的活动，但必须告知志愿者社团正处于筹建阶段，且社团不得收取活动成本以外的任何会费和其他费用。
        <br />&emsp;试运行结束前，社团发起人正式拟定好社团章程并提交试运行总结报告。
        <br />&emsp;五 社团成立审批简介
        <br />&emsp;筹建社团完成试运行，并提交完整成立申请材料后（准备材料如下），行政监察部将对社团管理层的构建、活动跟进了解得到情况以及社团章程等进行初步审议。如初步审议通过，将在一周内安排正式的成立审批。届时，社团方面需由主要管理层2-4名成员到场，其中必须包括会长。成立审批将以答辩的方式深入审查待建社团的各方面情况，以考察其是否具备成立的资质。审批结果会在一周内通知社团。一旦审批通过，社团即正式成立，进入正常运行阶段。
        <br />&emsp;附：社团成立申请材料：
        <br />&emsp;1、社团章程
        <br />&emsp;2、试运行总结报告
        <br />&emsp;3、社团挂靠协议
        <br />&emsp;4、《学生社团审批表》
        <br />&emsp;六 学生俱乐部帮扶计划
        <br />&emsp;学生社团联合会为充分发展校园文化，丰富校园生活，提出了对校园内学生俱乐部的帮扶计划，并呼吁我校各俱乐部成为正式校级注册社团，共同为校园文化注入新的活力。学生社团联合会权益部也制定了快速注册方案，帮助民间俱乐部快速转型并正式注册。
        <br />&emsp;如果你们有：
        <br />&emsp;1、20人以上的固定会员；
        <br />&emsp;2、已进行五个月以上的常规活动；
        <br />&emsp;3、较为完备的管理层；
        <br />&emsp;4、其他相关的成熟的社团发展模式。
