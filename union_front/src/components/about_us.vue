        <br />&emsp;1、联系挂靠院系与指导老师；
        <br />&emsp;2、举办一到两次活动；
        <br />&emsp;3、完善管理层的构建；
        <br />&emsp;4、完善各项规章制度。
        <br />&emsp;以上工作结束后，权益部将就社团管理模式的转型、活动跟进了解得到的情况以及社团章程决定准备工作是否合格。如合格，权益部将尽快对社团进行成立审批。
