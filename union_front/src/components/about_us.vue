        <br />&emsp;社团章程制定说明
        <br />&emsp;校级社团，区别于可以随时聚散的民间团队，必须有一定的组织性、传承性，能够稳定、长久的发展。因此，社团需要有一份完备的章程来予以保证。以下是社团章程模板：
        <br />&emsp;华中科技大学XX协会章程
        <br />&emsp;一、总则
        <br />&emsp;此章主要说明社团的特色及创办目的，为社团的发展制定一个明确的方向。
        <br />&emsp;主要内容：社团名称、性质及宗旨，与社团的挂靠单位及指导老师
        <br />&emsp;二、组织框架
        此章主要说明社团的管理机制。完善的管理层组织方式及职能分配是协会活动开展的前提。公平、公正、公开的干部产生机制能充分利用社团的人才资源。良好的会员培养计划与维权投诉机制也能减少后期的会员流失，使社团更加蓬勃发展。
        <br />&emsp;主要内容：社团管理层的结构及职能分配、社团干部产生机制、会员的权利与义务，与会员维权投诉机制
        <br />&emsp;三、活动规划
        <br />&emsp;良好的活动规划是社团稳定运行的基础，其中日常活动规划尤为重要。而其关键在于场地与内容。而品牌活动对增强社团内部凝聚力与扩大对外影响力有着极其重要的作用。
        <br />&emsp;主要内容：1、日常活动：活动周期、活动地点及主要活动形式
        <br />&emsp;2、品牌活动：活动时间、面向对象及活动形式
        <br />&emsp;四、财务外联
        <br />&emsp;社团必须有专人负责对财务的日常管理，并且定期向会员进行财务公示。社团应注意钱账分管，并且会长不得参与钱款管理。
        <br />&emsp;主要内容：社团经费使用制度、报账制度、财务监督制度、财务公开制度
        <br />&emsp;以上内容皆为行政监察部根据校内优秀社团章程总结而来，社团可在此基础上根据社团特色及试运行经验做适当调整，积极创新，做出符合社团特色的章程。
