        <br />&emsp;每年5月至6月，社联行政监察部组织对全校在册社团进行大型评估，由社联各部门部分成员组成的评估委员会按照最新评估制度进行公正公平的打分评估。而全校社团也借此机会对自己的工作与发展成果进行整体总结，积极为评估准备。
        <br />&emsp;6月中旬社联会举办大型优秀社团答辩会和优秀社团颁奖晚会。在前期评估中得分前三十名优秀社团将在优秀社团答辩会中进行答辩，由社员委员会、学生社团联合会和其他校级学生组织组成的评优委员会对其答辩评优，选出全校十佳社团。而随后进行的优秀社团颁奖晚会则对所有获奖社团进行颁奖，是所有社团人的分享荣誉与喜悦的时刻，是学校对社团工作的充分肯定。
        <br />&emsp;社团进军营
